class RiskConfig:
    """风险判定标准常量类"""

    # --- 权重配置 (总和为 1.0) ---
    WEIGHTS = {
        'volatility': 0.4,  # 波动率权重最高，反映日常不安分程度
        'max_drawdown': 0.5,  # 最大回撤，反映极端亏损风险
        'beta': 0,  # Beta，反映与大盘的联动性
        'sharpe': 0.1  # 夏普比率，反映风险收益性价比
    }

    # --- 风险阈值定义 (低风险上限, 高风险下限) ---
    # 波动率 (年化)
    VOL_LIMITS = (0.02, 0.04)  # <2%低, >4%高

    # 最大回撤 (绝对值)
    MDD_LIMITS = (0.005, 0.02)  # <15%低, >35%高

    # Beta 系数
    BETA_LIMITS = (0.80, 1.20)  # <0.8低(防御), >1.2高(进攻)

    # 夏普比率 (注意：夏普越高风险性价比越好，所以逻辑是反的)
    SHARPE_LIMITS = (1.0, 0.2)  # >1.0低风险感, <0.2高风险感
